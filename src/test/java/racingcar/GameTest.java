package racingcar;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

public class GameTest {
    private Cars cars;
    private GameInfo gameInfo;
    private Game game;

    @Test
    @DisplayName("자동차_2대_횟수_1회_검증")
    void 자동차_2대_횟수_1회_검증() {
        cars = new Cars("pobi,woni");
        gameInfo = new GameInfo("1");
        game = new Game(cars, gameInfo);

        Assertions.assertDoesNotThrow(() -> game.play());
    }

    @Test
    @DisplayName("자동차_3대_횟수_5회_검증")
    void 자동차_3대_횟수_5회_검증() {
        cars = new Cars("pobi,woni,gobi");
        gameInfo = new GameInfo("5");
        game = new Game(cars, gameInfo);

        Assertions.assertDoesNotThrow(() -> game.play());
    }
}
